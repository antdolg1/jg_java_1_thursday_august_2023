public class Task_9Trader {
    public static class Trader {
        private String fullName;
        private String city;

        public Trader(String fullName, String city) {
            this.fullName = fullName;
            this.city = city;
        }

        public String getFullName() {
            return fullName;
        }

        public String getCity() {
            return city;
        }
    }
}
