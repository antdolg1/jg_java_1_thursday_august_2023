public class Num3 {
    public static void main(String[] args) {
        int [] nums = {1,2,3};

        //вывод всех элементов массива в консоль
        for (int i = 0; i < nums.length; i++) {
            System.out.println("[" + i + "]" + " = " + nums[i]);
        }
    }
}
