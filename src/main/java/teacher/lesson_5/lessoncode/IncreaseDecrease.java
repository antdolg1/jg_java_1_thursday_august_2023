package teacher.lesson_5.lessoncode;

public class IncreaseDecrease {
    public static void main(String[] args) {

        int number = 0;

        System.out.println("Number value is: " + number);

        //1 способ
        number = number + 1;

        //2 способ
        number++;

        //3 способ
        number += 1;



        //1 способ
        number = number - 3;

        //2 способ
        number--;
        number--;
        number--;

        //3 способ
        number -= 3;


    }
}
